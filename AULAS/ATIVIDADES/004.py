# Tipo de dado bool (boolean)

# Ao questionar algo em um programa, só existem duas respostas possíveis:
# sim (True) ou não (False)

# Existem vários operadores para "questionar", dentre eles o ==, que é
# um operador lógico que questiona se um operador é igual a outro.

print(10 == 10)
print(10 == 11)

print(type(10 == 10))
print(type(10 == 11))
print(type(True))
print(type(False))
